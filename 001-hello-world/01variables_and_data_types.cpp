#include <iostream>
#include <string>

using namespace std;

int main() {
    // Variable declaration
    int age;
    double salary;
    char gender;
    string name;

    // Variable initialization
    age = 20;
    salary = 50000.00;
    gender = 'M';
    name = "Arjun";

    // Displaying variables
    cout << "Name: " << name << endl;
    cout << "Age: " << age << endl;
    cout << "Salary: " << salary << endl;
    cout << "Gender: " << gender << endl;

    return 0;
}

